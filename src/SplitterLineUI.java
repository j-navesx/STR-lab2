    private void emergency_meeting(){
        InputStream sound;
        try{
            sound = new FileInputStream(new File("./sounds/EmergencyMeeting.wav"));
            AudioStream audio = new AudioStream(sound);
            AudioPlayer.player.start(audio);
        }
        catch(Exception e){
            System.out.println("Failed." + e);
        }
    }
    
